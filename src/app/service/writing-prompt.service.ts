  generate_prompts(keyword = 'keyword', num = 10, next) {
    const shuffled = this.prompts.slice(0);
    let i = this.prompts.length;
    const min = i - num;
    let temp = '';
    let index = 0;
    let final_arr: string[];
    while (i-- > min) {
        index = Math.floor((i + 1) * Math.random());
        temp = shuffled[index];
        temp = this._replace_template(temp, keyword);
        shuffled[index] = shuffled[i];
        shuffled[i] = temp;
    }
    final_arr = shuffled.slice(min);
    // console.log('final_arr is of length ' + final_arr.length, final_arr);
    next(null, final_arr);
  }

  private _replace_template(str: string, keyword: string) {
    str = str.replace(/##KEYWORD##/g, keyword);

    // replace year
    const year = new Date().getFullYear().toString();
    str = str.replace(/##YEAR##/g, year);

    // generate a random number between 3 to 10
    const rand = Math.floor(10 * Math.random()) + 3;
    const rand_str = rand.toString();
    str = str.replace(/##COUNT##/g, rand_str);

    // randomly add 'in year' to headlines
    const rand_1 = Math.floor(100 * Math.random()) + 1;
    if ( rand_1 % 7 === 0) {
      str = str + ' in ' + year;
    }

    return str;
  }

